#資料: 程式的基本單位

#數字
1234
3.1

#字串
"我是梅西"
"I AM MESSI"

#布林值
True
False

#有順序、可動的列表 List
[3,4,5]
["Messi","梅西"]

#有順序、不可動的列表 Tuple
(1,2,3)
("Messi","梅西")

#集合 Set 沒區分誰是第一個第二個第三個
{7,8,9}
{"Messi","梅西"}

#字典 Dictoinary
{"Messi":"梅西","Jordan":"喬登"}

